def plot_final_running_avg_vs_rho(running_averages, rhos, steps=24):
    """Plot final running average values as a function of ρ"""
    plt.figure(figsize=(12, 6))

    final_avg = running_averages[:, -1]

    plt.plot(rhos, final_avg, 'bo-', markersize=6, linewidth=2)
    plt.xlabel('ρ Value')
    plt.ylabel(f'Final Running Average (after {steps} steps)')
    plt.title('Asymptotic Running Average Entropy vs Initial Coin Parameter ρ')
    plt.grid(True, alpha=0.3)

    # Fit a curve to see the pattern
    try:
        # Try fitting a polynomial
        z = np.polyfit(rhos, final_avg, 4)
        p = np.poly1d(z)
        rhos_smooth = np.linspace(min(rhos), max(rhos), 100)
        plt.plot(rhos_smooth, p(rhos_smooth), 'r-', alpha=0.5,
                label=f'Polynomial fit (degree 4)')

        # Calculate and display fit statistics
        residuals = final_avg - p(rhos)
        ss_res = np.sum(residuals**2)
        ss_tot = np.sum((final_avg - np.mean(final_avg))**2)
        r_squared = 1 - (ss_res / ss_tot)
        plt.text(0.05, 0.95, f'R² = {r_squared:.4f}',
                transform=plt.gca().transAxes, fontsize=12,
                verticalalignment='top')
    except:
        pass

    plt.legend()
    plt.tight_layout()
    plt.savefig('final_avg_vs_rho.png', dpi=150, bbox_inches='tight')
    plt.show()


def create_detailed_table(entropy_results, running_averages, rhos, steps=24):
    """Create a detailed pandas DataFrame with both raw entropy and running averages"""

    # Create multi-index columns
    columns = pd.MultiIndex.from_product([['Raw Entropy', 'Running Avg'],
                                         [f'Step {i}' for i in range(steps + 1)]])

    # Prepare data
    data = []
    for i in range(len(rhos)):
        row = []
        for step in range(steps + 1):
            row.append(entropy_results[i, step])
        for step in range(steps + 1):
            row.append(running_averages[i, step])
        data.append(row)

    df = pd.DataFrame(data, columns=columns, index=[f'ρ={r:.4f}' for r in rhos])

    # Add summary statistics
    df[('Summary', 'Final Raw')] = entropy_results[:, -1]
    df[('Summary', 'Final Running Avg')] = running_averages[:, -1]
    df[('Summary', 'Raw Mean')] = np.mean(entropy_results, axis=1)
    df[('Summary', 'Raw Std')] = np.std(entropy_results, axis=1)

    return df

def analyze_patterns(entropy_results, running_averages, rhos, N=8):
    """Analyze patterns in the running averages"""
    print("\n" + "="*80)
    print(f"RUNNING AVERAGE PATTERN ANALYSIS FOR {N}-CYCLE")
    print("="*80)

    # 1. Find ρ that gives highest and lowest final running average
    final_avg = running_averages[:, -1]
    max_idx = np.argmax(final_avg)
    min_idx = np.argmin(final_avg)

    print(f"\n1. EXTREME VALUES:")
    print(f"   Maximum final running average: {final_avg[max_idx]:.6f} at ρ = {rhos[max_idx]:.4f}")
    print(f"   Minimum final running average: {final_avg[min_idx]:.6f} at ρ = {rhos[min_idx]:.4f}")

    # 2. Check symmetry around ρ = 0.5
    print(f"\n2. SYMMETRY ANALYSIS:")
    mid = len(rhos) // 2
    symmetric = True
    for i in range(mid):
        if not np.isclose(final_avg[i], final_avg[-(i+1)], rtol=1e-3):
            symmetric = False
            print(f"   Asymmetry: ρ={rhos[i]:.4f} ({final_avg[i]:.6f}) vs ρ={rhos[-(i+1)]:.4f} ({final_avg[-(i+1)]:.6f})")
    if symmetric:
        print("   Running averages are symmetric around ρ = 0.5 ✓")

    # 3. Identify plateau regions (ρ values with similar final averages)
    print(f"\n3. PLATEAU IDENTIFICATION:")
    # Group similar final averages
    sorted_indices = np.argsort(final_avg)
    diffs = np.diff(final_avg[sorted_indices])
    threshold = 0.01  # 1% difference threshold

    plateaus = []
    current_plateau = [sorted_indices[0]]

    for i in range(1, len(diffs)):
        if diffs[i-1] < threshold:
            current_plateau.append(sorted_indices[i])
        else:
            if len(current_plateau) > 1:
                plateaus.append(current_plateau)
            current_plateau = [sorted_indices[i]]

    if len(current_plateau) > 1:
        plateaus.append(current_plateau)

    for i, plateau in enumerate(plateaus):
        rho_values = [rhos[idx] for idx in plateau]
        avg_values = [final_avg[idx] for idx in plateau]
        print(f"   Plateau {i+1}: ρ ∈ [{min(rho_values):.4f}, {max(rho_values):.4f}]")
        print(f"               Running avg ≈ {np.mean(avg_values):.6f} ± {np.std(avg_values):.6f}")

    # 4. Compare with theoretical predictions
    print(f"\n4. THEORETICAL COMPARISON:")
    # Theoretical maximum entropy for 2-level system is 1 bit
    print(f"   Theoretical maximum entropy: 1.000000 bit")
    print(f"   Achieved maximum running avg: {np.max(final_avg):.6f}")
    print(f"   Ratio: {np.max(final_avg):.2%}")

    # 5. Convergence analysis
    print(f"\n5. CONVERGENCE ANALYSIS:")
    # Measure how much the running average changes in last 5 steps
    late_changes = np.mean(np.abs(np.diff(running_averages[:, -5:], axis=1)), axis=1)
    best_convergence_idx = np.argmin(late_changes)
    worst_convergence_idx = np.argmax(late_changes)

    print(f"   Best convergence at ρ = {rhos[best_convergence_idx]:.4f}")
    print(f"   Worst convergence at ρ = {rhos[worst_convergence_idx]:.4f}")

    # 6. Periodicity in running average
    print(f"\n6. PERIODICITY DETECTION:")
    from scipy.signal import find_peaks

    for rho_val in [0.25, 0.5, 0.75]:
        idx = np.argmin(np.abs(rhos - rho_val))
        running_avg = running_averages[idx]

        # Find peaks in the running average derivative
        deriv = np.diff(running_avg)
        peaks, properties = find_peaks(np.abs(deriv), height=0.01)

        if len(peaks) > 0:
            avg_period = np.mean(np.diff(peaks))
            print(f"   ρ = {rho_val:.2f}: Characteristic period ≈ {avg_period:.1f} steps")
        else:
            print(f"   ρ = {rho_val:.2f}: No clear periodicity detected")


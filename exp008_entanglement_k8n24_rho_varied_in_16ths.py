def create_quantum_walk_operator(N, coin_type='parameterized', rho=0.5):
    """Create the quantum walk evolution operator for an N-cycle"""
    # Coin operator based on type
    if coin_type == 'hadamard':
        C = np.array([[1, 1], [1, -1]]) / np.sqrt(2)
    elif coin_type == 'parameterized':
        # General SU(2) coin: [√ρ, √(1-ρ); √(1-ρ), -√ρ]
        C = np.array([[np.sqrt(rho), np.sqrt(1-rho)],
                      [np.sqrt(1-rho), -np.sqrt(rho)]])
    else:
        raise ValueError("Coin type must be 'hadamard' or 'parameterized'")

    # Shift operator for position space
    S = np.zeros((2*N, 2*N), dtype=complex)

    for i in range(N):
        # Spin up state (|0⟩) moves to right (i+1 mod N)
        S[2*((i+1)%N), 2*i] = 1
        # Spin down state (|1⟩) moves to left (i-1 mod N)
        S[2*((i-1)%N) + 1, 2*i + 1] = 1

    # Full evolution operator: U = S (I ⊗ C)
    I_N = np.identity(N)
    full_coin = np.kron(I_N, C)
    U = S @ full_coin

    return U

def initialize_state(N, rho):
    """Initialize the quantum walk state with parameter rho"""
    psi = np.zeros(2*N, dtype=complex)

    # Start at position 0 with coin state determined by rho
    psi[0] = np.sqrt(rho)      # Spin up at position 0
    psi[1] = np.sqrt(1 - rho)   # Spin down at position 0

    # Normalize
    psi = psi / np.linalg.norm(psi)

    return psi

def compute_entanglement_entropy(state, N):
    """Compute the entanglement entropy between position and coin"""
    # Reshape state into matrix form: rows = position, cols = coin
    psi_matrix = state.reshape(N, 2)

    # Compute reduced density matrix for coin
    rho_coin = psi_matrix.conj().T @ psi_matrix

    # Compute eigenvalues
    eigenvalues = np.linalg.eigvalsh(rho_coin)

    # Handle numerical errors
    eigenvalues = np.clip(eigenvalues, 0, 1)
    eigenvalues = eigenvalues[eigenvalues > 1e-10]

    # Compute von Neumann entropy
    if len(eigenvalues) == 0:
        return 0.0
    entropy = -np.sum(eigenvalues * np.log2(eigenvalues))

    return entropy

def compute_running_average(data):
    """Compute running average (cumulative mean) of a 1D array"""
    n = len(data)
    running_avg = np.zeros(n)
    for i in range(n):
        running_avg[i] = np.mean(data[:i+1])
    return running_avg

def run_experiment(N=8, steps=24, rhos=None):
    """Run the quantum walk experiment for different rho values"""
    if rhos is None:
        rhos = np.linspace(1/16, 1, 16)

    # Store entropy for each rho and step
    entropy_results = np.zeros((len(rhos), steps + 1))
    running_averages = np.zeros((len(rhos), steps + 1))

    for i, rho in enumerate(rhos):
        print(f"Processing ρ = {rho:.4f}...")

        # Create evolution operator with parameterized coin
        U = create_quantum_walk_operator(N, 'parameterized', rho)

        # Initialize state
        psi = initialize_state(N, rho)

        # Store entropy for each step
        entropy_sequence = []

        # Step 0
        entropy = compute_entanglement_entropy(psi, N)
        entropy_sequence.append(entropy)

        # Evolve step by step
        for step in range(1, steps + 1):
            psi = U @ psi
            entropy = compute_entanglement_entropy(psi, N)
            entropy_sequence.append(entropy)

        entropy_results[i] = entropy_sequence
        running_averages[i] = compute_running_average(entropy_sequence)

    return entropy_results, running_averages

